# Initialize a string
text = " Hello, World! "

# Demonstrate various string methods
print("Original string:", text)
print("Strip leading and trailing spaces:", text.strip())
print("Convert to uppercase:", text.upper())
print("Convert to lowercase:", text.lower())
print("Swap case:", text.swapcase())
print("Title case:", text.title())
print("Replace 'World' with 'Python':", text.replace("World", "Python"))
print("Find the index of 'World':", text.find("World"))
print("Check if all characters are alphabetic:", text.isalpha())
print("Check if all characters are digits:", text.isdigit())
print("Split string into a list of words:", text.split())
print("Check if string starts with 'Hello':", text.startswith("Hello"))
print("Check if string ends with 'World!':", text.endswith("World!"))
