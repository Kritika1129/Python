import numpy as np

# Creating 1D Arrays
array_a = np.array([1, 2, 3])
array_b = np.array([4, 5, 6])

# Element-wise addition of two arrays
sum_array = array_a + array_b

# Calculating the mean of elements in array_a
mean_value = np.mean(array_a)

# Calculating the standard deviation of the elements in array_a
std_dev = np.std(array_a)

# Display results
print(f"Array sum: {sum_array}")
print(f"Mean of array: {mean_value}")
print(f"Standard deviation: {std_dev}")
